/* ------------------------------------------------------------------ helpers */

export type PlatformKey = "tiktok" | "instagram" | "youtube" | "x";

const PLATFORM_ICONS: Record<PlatformKey, (p: IconProps) => React.ReactElement> = {
  tiktok: TikTokIcon,
  instagram: InstagramIcon,
  youtube: YouTubeIcon,
  x: XIcon,
};

/** Brand-colour text class so each logo renders in its real colour. */
export const PLATFORM_BRAND_CLASS: Record<PlatformKey, string> = {
  tiktok: "text-foreground",
  instagram: "text-[#E4405F]",
  youtube: "text-[#FF0000]",
  x: "text-foreground",
};

/**
 * Renders a platform's real logo. Falls back to null for unknown keys so
 * callers can decide what to show instead.
 */
export function PlatformIcon({
  platform,
  className,
}: {
  platform: string;
  className?: string;
}) {
  const Icon = PLATFORM_ICONS[platform as PlatformKey];
  return Icon ? <Icon className={className} /> : null;
}
